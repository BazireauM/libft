void	*ft_memchr(void *m, int c, int size)
{
	int		i;
	char	*m2;

	m2 = (char *) m;
	i = 0;
	while (i < size)
	{
		if (m2[i] == (char) c)
			return (&m2[i]);
		i++;
	}
	return (0);
}
