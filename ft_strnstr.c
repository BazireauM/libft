char	*ft_strnstr(char *s1, char *s2, int n)
{
	int	i;
	int	a;
	int	b;
	int	d;

	d = 0;
	while (s2[d] != '\0')
		d++;
	i = 0;
	while (i < n)
	{
		b = 0;
		a = 0;
		while (i + b < n && s2[b] != '\0')
		{
			if (s2[b] == s1[i + b])
				a++;
			b++;
		}
		if (a == d)
			return (&s1[i]);
		i++;
	}
	return (0);
}
