char	*ft_strrchr(char *s, int c)
{
	int		i;
	char	*a;

	a = 0;
	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == (char) c)
			a = &s[i];
		i++;
	}
	if (s[i] == (char) c)
		a = &s[i];
	return (a);
}
