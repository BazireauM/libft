int	ft_strlcat(char *dst, const char *src, int size)
{
	int	i;
	int	c;

	i = 0;
	c = 0;
	while (dst[i] != '\0')
	{
		i++;
	}
	while (i < size - 1)
	{
		dst[i] = src[c];
		i++;
		c++;
	}
	dst[i] = '\0';
	return (size);
}
