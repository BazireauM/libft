int	ft_memcmp(void *p1, void *p2, int size)
{
	char	*a1;
	char	*a2;
	int		i;

	a1 = (char *) p1;
	a2 = (char *) p2;
	i = 0;
	while (i < size)
	{
		if (a1[i] < a2[i])
			return (-1);
		if (a1[i] > a2[i])
			return (1);
		i++;
	}
	return (0);
}
