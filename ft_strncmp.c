int	ft_strncmp(const char *s1, const char *s2, int size)
{
	int	i;

	i = 0;
	while (i < size)
	{
		if (s1[i] < s2[i])
			return (-1);
		if (s1[i] > s2[i])
			return (1);
		i++;
	}
	return (0);
}
